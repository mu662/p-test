var mongoose = require('mongoose');
var Schema = mongoose.Schema;
//brands Schema
var usersDataSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    number: {
        type: String,
        required: true
    },
    created_at: {
        type: Date,
        default: Date.now()
    },
    updated_at: {
        type: Date,
        default: Date.now()
    }
});
var collectionName = 'users'
module.exports = mongoose.model('users', usersDataSchema, collectionName);